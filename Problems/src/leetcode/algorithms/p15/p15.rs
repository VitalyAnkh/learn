pub fn three_sum(nums: Vec<i32>) -> Vec<Vec<i32>> {
    if nums.len() < 3 {
        return Vec::new();
    }
    let mut nums2 = nums.clone();
    nums2.dedup();
    if nums2.len() == 1 {
        if nums2[0] == 0 {
            let nums = nums2;
        } else {
            return Vec::new();
        }
    }

    let length = nums.len();
    let mut v = Vec::new();
    for i in 0..length {
        for j in (i + 1)..length {
            if j != length {
                for k in j..length {
                    if nums[i] + nums[j] + nums[k] == 0 {
                        let mut t1 = vec![nums[i], nums[j], nums[k]];
                        t1.sort();

                        if (v.is_empty()) {
                            v.push(t1);
                        } else {
                            if v[v.len() - 1] != t1 {
                                v.push(t1);
                            }
                        }
                    }
                }
            }
        }
    }
    v.sort();
    v.dedup();
    v
}

